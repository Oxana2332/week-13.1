function calcSum(){
    let a = prompt("Введите первое число");
    let b = prompt("Введите второе число");
    return Number(a) + Number(b);
}

function calcMinus(){
    let a = prompt("Введите первое число");
    let b = prompt("Введите второе число");
    return a - b;
}

function calcMulti(){
    let a = prompt("Введите первое число");
    let b = prompt("Введите второе число");
    return a * b;
}

function calcDivide(){
    let a = prompt("Введите первое число");
    let b = prompt("Введите второе число");
    return a / b;
}